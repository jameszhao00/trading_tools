def calculate_for_one_ticker(output_directory: str, ticker: str):
    print("Processing ", ticker)
    backtester = Backtester(datetime(2019, 1, 1, 0, 0, 0), start_when_all_data_is_available=True)
    r = backtester.backtest([ticker], FACTOR_UNIVERSE, min_variance_portfolio)
    alpha_csv_path = join(output_directory, ticker + "_alpha_returns.csv")
    r.returns_history.rename("alpha").to_csv(alpha_csv_path)
    loadings_csv_path = join(output_directory, ticker + "_factor_loadings.csv")
    r.allocation_history.to_csv(loadings_csv_path)


@click.command()
@click.option('--output_directory', prompt='Output directory', default="./returns_analysis")
def calculate_return_factors(output_directory: str):
    Parallel(n_jobs=16)(delayed(calculate_for_one_ticker)(output_directory, ticker) for ticker in set(UNIVERSE))


